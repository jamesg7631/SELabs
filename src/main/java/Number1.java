public class Number1 {
    private int numberA;
    private int numberB;
    private int numberC;

    public Number1(int numberA, int numberB, int numberC) {
        this.numberA = numberA;
        this.numberB = numberB;
        this.numberC = numberC;
    }

    public int getNumberA() {
        return this.numberA;
    }

    public int getNumberB() {
        return this.numberB;
    }

    public int getNumberC() {
        return this.numberC;
    }
}
